package day06_Concatination;

public class C02_Concatenation {
    public static void main(String[] args) {
        String a="Hello";
        String b="World";
        System.out.println(a+b);
        System.out.println(a+ " " +b);


        String c="hello";
        int d=1;
        int e=2;

        System.out.println(c+d+e);
        System.out.println(e+d+c);


    }
}
